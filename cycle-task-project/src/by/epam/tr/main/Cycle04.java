package by.epam.tr.main;

public class Cycle04 {

	public static void main(String[] args) {
		// с помощью оператора while вывести все четные числа
		//в диапазоне от 2 до 100 включительно.
		
		int x = 2;
		
		while(x <= 100) {
			System.out.print(x + " ");
			x = x + 2;
		}
		
		
	}

}
