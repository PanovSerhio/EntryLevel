package by.epam.tr.main;

public class Line01 {

	public static void main(String[] args) {
		// даны два действит. числа x и y. вычислить их сумму, разность, произведение и частное.
		
		double x = 6;
		double y = 4;
		
		System.out.println("сумма чисел = " + (x + y));
		System.out.println("разность чисел = " + (x - y));
		System.out.println("произведение чисел = " + (x * y));
		System.out.println("частное чисел = " + (x / y));

	}

}
